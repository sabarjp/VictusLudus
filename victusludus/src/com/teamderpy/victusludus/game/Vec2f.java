package com.teamderpy.victusludus.game;


/**
 * The Class Vec2f.
 */
public class Vec2f {
	
	/** The x. */
	public float x;
	
	/** The y. */
	public float y;
	
	/**
	 * Instantiates a new vec2f.
	 *
	 * @param x the x
	 * @param y the y
	 */
	public Vec2f(float x, float y){
		this.x = x;
		this.y = y;
	}
}
