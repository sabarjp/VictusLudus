package com.teamderpy.victusludus.game;


/**
 * The Class ScreenCoord.
 */
public class ScreenCoord {
	
	/** The x. */
	public int x;
	
	/** The y. */
	public int y;
	
	/**
	 * Instantiates a new screen coord.
	 *
	 * @param x the x
	 * @param y the y
	 */
	public ScreenCoord(int x, int y){
		this.x = x;
		this.y = y;
	}
}
