package com.teamderpy.victusludus.parts;


/**
 * The Class Material.
 */
public class Material {
	
	/** The id. */
	private String id;
	
	/** The name. */
	private String name = "";
	
	/** The description. */
	private String description = "";
	
	/** The density. */
	private int density = 100;
	
	/** The mass. */
	private int mass = 100;
	
	/** The hardness. */
	private int hardness = 100;
	
	/** The elasticity. */
	private int elasticity = 100;
	
	/** The rigidity. */
	private int rigidity = 100;
	
	/** The compressibility. */
	private int compressibility = 100;
	
	/** The toughness. */
	private int toughness = 100;
	
	/** The melting point. */
	private int meltingPoint = 4000;
	
	/** The boiling point. */
	private int boilingPoint = 8000;

	/**
	 * Gets the name.
	 *
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * Sets the name.
	 *
	 * @param name the new name
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * Gets the description.
	 *
	 * @return the description
	 */
	public String getDescription() {
		return description;
	}

	/**
	 * Sets the description.
	 *
	 * @param description the new description
	 */
	public void setDescription(String description) {
		this.description = description;
	}

	/**
	 * Gets the density.
	 *
	 * @return the density
	 */
	public int getDensity() {
		return density;
	}

	/**
	 * Sets the density.
	 *
	 * @param density the new density
	 */
	public void setDensity(int density) {
		this.density = density;
	}

	/**
	 * Gets the mass.
	 *
	 * @return the mass
	 */
	public int getMass() {
		return mass;
	}

	/**
	 * Sets the mass.
	 *
	 * @param mass the new mass
	 */
	public void setMass(int mass) {
		this.mass = mass;
	}

	/**
	 * Gets the hardness.
	 *
	 * @return the hardness
	 */
	public int getHardness() {
		return hardness;
	}

	/**
	 * Sets the hardness.
	 *
	 * @param hardness the new hardness
	 */
	public void setHardness(int hardness) {
		this.hardness = hardness;
	}

	/**
	 * Gets the toughness.
	 *
	 * @return the toughness
	 */
	public int getToughness() {
		return toughness;
	}

	/**
	 * Sets the toughness.
	 *
	 * @param toughness the new toughness
	 */
	public void setToughness(int toughness) {
		this.toughness = toughness;
	}

	/**
	 * Gets the id.
	 *
	 * @return the id
	 */
	public String getId() {
		return id;
	}

	/**
	 * Sets the id.
	 *
	 * @param id the new id
	 */
	public void setId(String id) {
		this.id = id;
	}

	/**
	 * Gets the melting point.
	 *
	 * @return the melting point
	 */
	public int getMeltingPoint() {
		return meltingPoint;
	}

	/**
	 * Sets the melting point.
	 *
	 * @param meltingPoint the new melting point
	 */
	public void setMeltingPoint(int meltingPoint) {
		this.meltingPoint = meltingPoint;
	}

	/**
	 * Gets the boiling point.
	 *
	 * @return the boiling point
	 */
	public int getBoilingPoint() {
		return boilingPoint;
	}

	/**
	 * Sets the boiling point.
	 *
	 * @param boilingPoint the new boiling point
	 */
	public void setBoilingPoint(int boilingPoint) {
		this.boilingPoint = boilingPoint;
	}

	/**
	 * Gets the elasticity.
	 *
	 * @return the elasticity
	 */
	public int getElasticity() {
		return elasticity;
	}

	/**
	 * Sets the elasticity.
	 *
	 * @param elasticity the new elasticity
	 */
	public void setElasticity(int elasticity) {
		this.elasticity = elasticity;
	}

	/**
	 * Gets the rigidity.
	 *
	 * @return the rigidity
	 */
	public int getRigidity() {
		return rigidity;
	}

	/**
	 * Sets the rigidity.
	 *
	 * @param rigidity the new rigidity
	 */
	public void setRigidity(int rigidity) {
		this.rigidity = rigidity;
	}

	/**
	 * Gets the compressibility.
	 *
	 * @return the compressibility
	 */
	public int getCompressibility() {
		return compressibility;
	}

	/**
	 * Sets the compressibility.
	 *
	 * @param compressibility the new compressibility
	 */
	public void setCompressibility(int compressibility) {
		this.compressibility = compressibility;
	}
}
